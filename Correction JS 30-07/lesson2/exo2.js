// Compteur de 0 à 100
for (let cycle = 0; cycle <= 100; cycle++) {
    console.log(cycle); // Affiche la valeur de i à chaque itération
}

// Compteur de 0 à 100 en comptant de deux en deux
for (let i = 0; i <= 100; i += 2) {
    console.log(i); // Affiche la valeur de i à chaque itération
}

// Compteur de 100 à 0 en décomptant de deux en deux
for (let i = 100; i >= 0; i -= 2) {
    console.log(i); // Affiche la valeur de i à chaque itération
}
