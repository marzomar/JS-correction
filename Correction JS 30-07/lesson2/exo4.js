// Déclaration de la variable 'position' initialisée à 'Belleville'
let position = prompt("Donnez moi votre position");

// Structure de contrôle 'switch' pour vérifier la valeur de 'position'
switch (position) {
    case 'Belleville':
        console.log("tu es sur la ligne 11 ou la ligne 2");
        break; // Arrête l'exécution des autres cas si ce cas est vrai
    case 'mairie des Lilas':
        console.log("tu es sur la ligne 11");
        break; // Arrête l'exécution des autres cas si ce cas est vrai
    case 'père Lachaise':
        console.log("tu es sur la ligne 3 ou 2");
        break; // Arrête l'exécution des autres cas si ce cas est vrai
    default:
        console.log("Je ne sais pas où tu es.");
        break; // Optionnel ici, mais utile si vous ajoutez plus de code après le switch
}

